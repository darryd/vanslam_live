function parse_int_or_return_zero(val) {

  var result = parseInt(val);
  if (isNaN(result))
    return 0;

  return result;
}

/*----------------------------------------------------------------------------------------------------------------------------------*/
function parse_float_or_return_zero(val) {

  var result = parseFloat(val);

  return isNaN(result) ? 0 : result;
}
